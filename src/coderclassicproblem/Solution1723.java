    public int[] findSquare(int[][] matrix) {
        if (matrix.length < 1 || (matrix.length == 1 && matrix[0][0] == 1)) {
            return new int[] {};
        }
        int[] result = new int[] {0, 0, 1};
        int[][] dp = new int[matrix.length][matrix.length];
        dp[0][0] = (matrix[0][0] == 0 ? 1 : 0);
        for (int i = 1; i < matrix.length; i++) {
            dp[0][i] = (matrix[0][i] == 0 ? 1 + dp[0][i - 1] : 0);
        }
        for (int i = 1; i < matrix.length; i++) {
            dp[i][0] = (matrix[i][0] == 0 ? 1 + dp[i - 1][0] : 0);
        }
       return result;
    }
    
    public int[] findStrictSquare(int[][] matrix) {
        if (matrix.length < 1 || (matrix.length == 1 && matrix[0][0] == 1)) {
            return new int[] {};
        }
        int[] result = new int[] {0, 0, 1};
        int[][] dp = new int[matrix.length][matrix.length];
        dp[0][0] = (matrix[0][0] == 0 ? 1 : 0);
        for (int i = 1; i < matrix.length; i++) {
            dp[0][i] = (matrix[0][i] == 0 ? 1 : 0);
        }
        for (int i = 1; i < matrix.length; i++) {
            dp[i][0] = (matrix[i][0] == 0 ? 1 : 0);
        }
        
        // TODO 当前计算方式为整个正方形内均为零
        for (int i = 1; i < matrix.length; i++) {
            for (int j = 1; j < matrix.length; j++) {
                if (matrix[i][j] == 1) {
                    dp[i][j] = 0;
                }
                dp[i][j] = Math.min(Math.min(dp[i][j - 1], dp[i - 1][j]), dp[i - 1][j - 1]) + 1;
                
                if (result[2] < dp[i][j]) {
                    result[2] = dp[i][j];
                    result[0] = i + 1 - result[2];
                    result[1] = j + 1 - result[2];
                }
            }
        }
        for (int[] temp : dp) {
            System.out.println(Arrays.toString(temp));
        }
        return result;
    }
    
